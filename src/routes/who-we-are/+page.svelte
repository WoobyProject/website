<h1>Who we are</h1>
<p>
	At Wooby, we are on a mission to combat malnutrition and its devastating effects on children’s health by providing accessible, high-precision weight monitoring tools. We help communities, healthcare workers, and NGOs track children’s growth and prevent malnutrition by creating an open-source, affordable baby weight scale, with plans to develop software to collect, analyze, and act on the data for better health insights.
	We integrate cutting-edge, yet affordable and widely available technologies, such as microcontrollers, 3D printing, electronics, and machine learning, to ensure a robust, reliable, and accurate solution for measuring the weight of children.
	Our focus is on low-resource settings worldwide, where malnutrition is most acute, and access to quality healthcare tools is limited. Wooby’s solution is designed to be sustainable, adaptable, and easy to manufacture with technology readily available in most places. We believe every child deserves to grow up strong and healthy. 
	That’s why we’re building an ecosystem where collaboration and shared knowledge drive innovation. By making our technology open-source, we empower communities to create lasting, scalable solutions to malnutrition, one child at a time.
</p>

<h1>Our mission</h1>
<p>
	Wooby’s mission is to harness technology, innovation, and collaboration to make high-precision children’s weight monitoring accessible anywhere in the world. As an open-source initiative, we empower communities and healthcare workers with the tools they need to detect and prevent malnutrition early—because every child deserves to grow strong and healthy.
</p>

