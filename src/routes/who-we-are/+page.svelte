<h1>Team Organization</h1>
<p>
	Wooby operates through a dynamic and collaborative structure that relies on the efforts of multiple stakeholders, including the Core Volunteering Team, the Governance Body, a thriving Community, and a network of Partners.
</p>
<h3>Core Volunteering Team:</h3>
<p>
	The Core Volunteering Team is the driving force behind Wooby's mission, comprising passionate individuals dedicated to developing, promoting, and scaling the solution. This team works across multiple areas, from the technical development of the high-precision weight scale to creating marketing strategies and communication efforts. Each volunteer brings unique skills, contributing to the project's growth and ensuring Wooby remains adaptable and impactful over time. As the heart of Wooby, the Core Volunteering Team operates in a collaborative and dynamic environment, continuously pushing boundaries to create accessible solutions for global health challenges.
</p>
<h3>Governance Body:</h3>
<p>
	Wooby is formally established as an association in France, providing a clear and structured governance framework to ensure transparency, accountability, and sustainability. The governance body includes decision-makers who are responsible for overseeing strategic directions, financial planning, and compliance. This structure ensures that Wooby's objectives are met with the support of its stakeholders, while remaining aligned with its mission and values.
</p>
<h3>Community:</h3>
<p>
	Wooby’s Community is the foundation of its growth and impact. Without a vibrant and engaged community, the project could not thrive. The community consists of passionate individuals, organizations, and groups who contribute in various ways, from offering technical expertise to sharing knowledge, resources, and local insights. Through open-source collaboration, Wooby encourages individuals and organizations to actively participate, contribute ideas, and share solutions, ensuring that the project remains relevant, accessible, and impactful. The community is the driving force behind Wooby's continued success and global reach.
</p>
<h3>Partners:</h3>
<p>
	Wooby’s Partners play a critical role in supporting the project’s development and scalability. Key partners include organizations such as NGOs like MEDAIR, who provide invaluable field expertise and real-world insights into the challenges of child malnutrition, and Airbus Foundation, who offer funding and strategic support. Additionally, Airbus ProtoSpace serves as a collaborative workspace and innovation hub, providing physical resources and a collaborative environment to foster the development of Wooby’s technology. Partners contribute resources, expertise, and networks that extend Wooby’s reach and impact worldwide.
</p>